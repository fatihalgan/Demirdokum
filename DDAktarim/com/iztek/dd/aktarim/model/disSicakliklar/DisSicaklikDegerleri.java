package com.iztek.dd.aktarim.model.disSicakliklar;

public class DisSicaklikDegerleri {
	private int maksimumDisSicaklik;
	private double disOzgulNem;
	private double gunlukSicaklikFarki;

	/**
	 * @return Returns the disOzgulNem.
	 */
	public double getDisOzgulNem() {
		return disOzgulNem;
	}

	/**
	 * @param disOzgulNem
	 *            The disOzgulNem to set.
	 */
	public void setDisOzgulNem(double disOzgulNem) {
		this.disOzgulNem = disOzgulNem;
	}

	/**
	 * @return Returns the gunlukSicaklikFarki.
	 */
	public double getGunlukSicaklikFarki() {
		return gunlukSicaklikFarki;
	}

	/**
	 * @param gunlukSicaklikFarki
	 *            The gunlukSicaklikFarki to set.
	 */
	public void setGunlukSicaklikFarki(double gunlukSicaklikFarki) {
		this.gunlukSicaklikFarki = gunlukSicaklikFarki;
	}

	/**
	 * @return Returns the maksimumDisSicaklik.
	 */
	public int getMaksimumDisSicaklik() {
		return maksimumDisSicaklik;
	}

	/**
	 * @param maksimumDisSicaklik
	 *            The maksimumDisSicaklik to set.
	 */
	public void setMaksimumDisSicaklik(int maksimumDisSicaklik) {
		this.maksimumDisSicaklik = maksimumDisSicaklik;
	}
}